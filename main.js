const dataContainer = document.querySelectorAll('.time-tracker .card .details .data');
function updateCards (filter='weekly'){
for (let i = 0; i < dataContainer.length; i++) {
  const e = data[i];
  const container = dataContainer[i];
  container.innerHTML = `
    <p class="current">${e.timeframes[filter].current}hrs</p>
    <p class="previous">Last ${filter==='daily'?'day': filter==='weekly'?'week': 'month'} - ${e.timeframes[filter].previous}hrs</p>
    `
  }
}

const btns = document.querySelectorAll('.filters button');
const clearActive = () => btns.forEach(btn => btn.classList.remove('active'))
btns.forEach(btn => {
  btn.addEventListener('click', () => {
    clearActive();
    btn.classList.add('active');
    updateCards(btn.dataset.filter);
  })
});


updateCards()